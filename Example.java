    public static double[] getRemovedDuplicateAvg(int[][] marks, double[] studentAvg) {

        double[] newStudentAvg = new double[marks.length];

        for (int i = 0; i < newStudentAvg.length; i++) {
            int prf = marks[i][0];
            int dbms = marks[i][1];
            newStudentAvg[i] = (double) (prf + dbms) / 2.0;
        }
//        System.out.println(Arrays.toString(newStudentAvg));


        for (int j = newStudentAvg.length - 1; j > 0; j--) {  //sorting max to min
            for (int i = 0; i < j; i++) {
                if (newStudentAvg[i] < newStudentAvg[i + 1]) {
                    double a = newStudentAvg[i];
                    newStudentAvg[i] = newStudentAvg[i + 1];
                    newStudentAvg[i + 1] = a;
                }
            }
        }
//        System.out.println(Arrays.toString(newStudentAvg));


        //remove duplicates

        if (newStudentAvg.length == 0 || newStudentAvg.length == 1) {
            return newStudentAvg;
        }

        // creating another array for only storing
        // the unique elements
        double[] temp = new double[newStudentAvg.length];
        int j = 0;

        for (int i = 0; i < newStudentAvg.length - 1; i++) {
            if (newStudentAvg[i] != newStudentAvg[i + 1]) {
                temp[j++] = newStudentAvg[i];
            }
        }

        temp[j++] = newStudentAvg[newStudentAvg.length - 1];

        // Changing the original array
        for (int i = 0; i < j; i++) {
            newStudentAvg[i] = temp[i];
        }


        double newRemovedArray[] = new double[j];

        for (int i = 0; i < newRemovedArray.length; i++) {
            newRemovedArray[i] = newStudentAvg[i]; // assign new duplicate removed values
        }
//        System.out.println(Arrays.toString(newRemovedArray));


        return newRemovedArray;
    }

